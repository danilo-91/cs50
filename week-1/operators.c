#include <stdio.h>

int main(void)
{
	int x = 10;
	int y = 15;

	printf("Sum of %d and %d\n", x, y);
	printf("%d\n", x + y);
	printf("Substraction of %d and %d\n", x, y);
	printf("%d\n", x - y);
	printf("Multiplication of %d and %d\n", x, y);
	printf("%d\n", x * y);
	printf("Division of %d and %d\n", y, x);
	printf("%d\n", y / x);
	return 0;
}
